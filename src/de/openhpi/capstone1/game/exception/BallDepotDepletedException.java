public class BallDepotDepletedException extends Exception {

	private static final long serialVersionUID = -66815291174793442L;

	public BallDepotDepletedException() {
		super();
	}

	public BallDepotDepletedException(String arg0) {
		super(arg0);
	}

	public BallDepotDepletedException(Throwable arg0) {
		super(arg0);
	}

	public BallDepotDepletedException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public BallDepotDepletedException(String arg0, Throwable arg1, boolean arg2, boolean arg3) {
		super(arg0, arg1, arg2, arg3);
	}

}
